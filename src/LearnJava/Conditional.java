public class Conditional {
	
	public static void main(String[] args) {
		int n = 2554145  ;
		
		if(n==0) 
		{
			System.out.println("nothing");
			System.out.println("Bye");
			
		}
		else if(n%2==0)
				System.out.println("Even");
		else
				System.out.println("Odd");
		
	}

}
